# index start 0
#   01234567890123456 +
s ="python programing"
#   76543210987654321 -

#only one index
print(s[3])

#start and end
print(s[3:13])
print(s[0:10])

#End with
#start with
print(s[:7])
print(s[5:])

#start,end,jump
print(s[0:15:3])

# direct jump
print(s[::5])

#index nagetive
print(s[-15:-2])
print(s[:-5])
print(s[-14:])
print(s[-16:-4:2])

#Reverse String
print(s[::-1])

#string copy
print(s[:])


